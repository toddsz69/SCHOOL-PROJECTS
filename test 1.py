#Sorting adalah proses menyusun data dalam urutan tertentu, 
# misalnya dari yang terkecil ke yang terbesar atau dari yang terbesar ke yang terkecil.

#Metode sorting:
#1. Bubble Sort. Metode ini membandingkan pasangan elemen yang berdekatan dan 
# menukarnya jika mereka dalam urutan yang salah. Proses ini diulang hingga seluruh daftar terurut. 
# Metode ini sederhana tapi tidak efisien untuk daftar besar.

#2. Selection Sort. Metode ini mencari elemen terkecil atau terbesar (tergantung pada urutan yang diinginkan) 
# dari daftar yang belum terurut, kemudian menukarnya dengan elemen pertama dari daftar yang belum terurut. 
# Proses ini diulang untuk elemen-elemen berikutnya hingga seluruh daftar terurut.

#3. Insertion Sort. Metode ini membangun urutan akhir satu item pada satu waktu. 
# Lalu memeriksa elemen-elemen dalam daftar dan menyisipkannya pada posisi yang benar di bagian yang sudah terurut. 
# Metode ini efisien untuk daftar kecil atau daftar yang hampir terurut.